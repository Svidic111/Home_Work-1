package Task19_Strings;

public class CharCount {
    private char value = '0';
    private int count = 0;

    public CharCount(char value) {
        this.value = value;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public char getValue() {
        return value;
    }

    public int getCount() {
        return count;
    }
}
